Sun::Sun(
	Shader* shader,
	const glm::mat4& model,
	const glm::vec3& sunLightColor
):
_shader(shader),
_model(model),
_sunLightColor(sunLightColor)
{
	glGenBuffers(1, &this->_VBO);
	glBindBuffer(GL_ARRAY_BUFFER, this->_VBO);
	glBufferData(GL_ARRAY_BUFFER, this->_lightCubeVerts.size() * sizeof(float), &this->_lightCubeVerts[0], GL_STATIC_DRAW);
	glGenVertexArrays(1, &this->_VAO);
	glBindVertexArray(this->_VAO);
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	this->_shader->use();
	this->_shader->setMat4("model", this->_model);
	this->_shader->setVec3("lightColor", this->_sunLightColor);
}

void Sun::draw()
{
	glBindVertexArray(this->_VAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
}
